    private void logFilename(String name) {
        if (name != null && !name.equals(lastFileMessage)) {
            addMessage(0, "  %s:", name);
            lastFileMessage = name;
        }
    }

    private static class ConvertEntry {
        final String from;
        final String to;

        ConvertEntry(String from, String to) {
            this.from = getEntryName(from);
            this.to = getEntryName(to);
        }

        boolean matches(String s) {
            return s.matches(from);
        }

        String replace(String s) {
            return s.replaceFirst("^" + from, to);
        }
    }
